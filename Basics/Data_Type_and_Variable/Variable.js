// Variable are used to store data in javascript.
// Variables are declared using var, let or const keyword.
// var is used to declare a variable, 
// let is used to declare a block scope variable and 
// const is used to declare a constant variable.


var x = 10; // declare a variable x and assign it a value 10
let y = 20; // declare a variable y and assign it a value 20
const z = 30; // declare a constant z and assign it a value 30
// console.log(x); // prints 10
// console.log(y); // prints 20
// console.log(z); // prints 30

//basic rule to declare  a variable in javascript
// 1. variable name should be a valid identifier
// 2. variable name should not be a reserved keyword
// 3. variable name should not start with a number
// 4. variable name should not contain a space or special character
// 5. variable name should be a camel case or underscore notation
// 6. variable name should not be too long, it should be short and meaningful
// 7. variable name should be a noun or a verb, it should not be a adjective or adverb

